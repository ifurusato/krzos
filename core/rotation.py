# An enum for expressing the directions of rotation.
#

from enum import Enum

# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
class Rotation(Enum):
    STOPPED           = ( 0, 'stopped' )
    CLOCKWISE         = ( 1, 'clockwise' )
    COUNTER_CLOCKWISE = ( 2, 'counter-clockwise' )

    # ignore the first param since it's already set by __new__
    def __init__(self, num, label):
        self._label = label

    # ┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈┈
    @property
    def label(self):
        return self._label

